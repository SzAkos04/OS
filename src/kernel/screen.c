#define COLOR(_r, _g, _b)                                                      \
    ((u8)((((_r) & 0x7) << 5) | (((_g) & 0x7) << 2) | (((_b) & 0x3) << 0)))

#define COLOR_R(_index) (((_index) >> 5) & 0x7)
#define COLOR_G(_index) (((_index) >> 2) & 0x7)
#define COLOR_B(_index) (((_index) >> 0) & 0x3)

#define COLOR_ADD(_index, _d)                                                  \
    __extension__({                                                            \
        __typeof__(_index) _c = (_index);                                      \
        __typeof__(_d) __d = (_d);                                             \
        COLOR(CLAMP(COLOR_R(_c) + __d, 0, 7), CLAMP(COLOR_G(_c) + __d, 0, 7),  \
              CLAMP(COLOR_B(_c) + __d, 0, 3));                                 \
    })

// VGA control port addresses
#define PALETTE_MASK 0x3C6
#define PALETTE_READ 0x3C7
#define PALETTE_WRITE 0x3C8
#define PALETTE_DATA 0x3C9

void screen_init() {
    // configure palette with 8-bit RRRGGGBB color
    outportb(PALETTE_MASK, 0xFF);
    outportb(PALETTE_WRITE, 0);
    for (u8 i = 0; i < 255; i++) {
        outportb(PALETTE_DATA, (((i >> 5) & 0x7) * (256 / 8)) / 4);
        outportb(PALETTE_DATA, (((i >> 2) & 0x7) * (256 / 8)) / 4);
        outportb(PALETTE_DATA, (((i >> 0) & 0x3) * (256 / 4)) / 4);
    }

    // set color 255 = white
    outportb(PALETTE_DATA, 0x3F);
    outportb(PALETTE_DATA, 0x3F);
    outportb(PALETTE_DATA, 0x3F);
}

void putpixel(int pos_x, int pos_y, u8 VGA_COLOR) {
    if (pos_x >= 0 && pos_x < SCREEN_WIDTH && pos_y >= 0 &&
        pos_y < SCREEN_HEIGHT) {
        u8 *location = (u8 *)VIDEO_ADDRESS + SCREEN_WIDTH * pos_y + pos_x;
        *location = VGA_COLOR;
    }
}

void fill_screen(u8 VGA_COLOR) {
    for (int x = 0; x < SCREEN_WIDTH; x++) {
        for (int y = 0; y < SCREEN_HEIGHT; y++) {
            putpixel(x, y, VGA_COLOR);
        }
    }
}

void clear_screen(void) { fill_screen(BLACK); }

static void draw_h_line(int x1, int x2, int y, u8 VGA_COLOR) {
    for (int x = x1; x <= x2; x++) {
        putpixel(x, y, VGA_COLOR);
    }
}

static void draw_v_line(int x, int y1, int y2, u8 VGA_COLOR) {
    for (int y = y1; y <= y2; y++) {
        putpixel(x, y, VGA_COLOR);
    }
}

void draw_line(int x1, int y1, int x2, int y2, u8 VGA_COLOR) {
    int dx = x2 - x1;
    int dy = y2 - y1;
    int steps = abs(dx) > abs(dy) ? abs(dx) : abs(dy);

    float xIncrement = dx / (float)steps;
    float yIncrement = dy / (float)steps;

    float x = x1;
    float y = y1;

    for (int i = 0; i <= steps; i++) {
        putpixel((int)x, (int)y, VGA_COLOR);
        x += xIncrement;
        y += yIncrement;
    }
}

void draw_rect(int x1, int y1, int x2, int y2, u8 VGA_COLOR) {
    draw_h_line(x1, x2, y1, VGA_COLOR);
    draw_h_line(x1, x2, y2, VGA_COLOR);
    draw_v_line(x1, y1, y2, VGA_COLOR);
    draw_v_line(x2, y1, y2, VGA_COLOR);
}

void fill_rect(int x1, int y1, int x2, int y2, u8 VGA_COLOR) {
    for (int y = y1; y <= y2; y++) {
        draw_h_line(x1, x2, y, VGA_COLOR);
    }
}

static void draw_char(char ch, int x, int y, u8 VGA_COLOR) {
    for (int i = 0; i < FONT_SIZE; i++) {
        for (int j = 0; j < FONT_SIZE; j++) {
            if (font[ch][i] & 1 << j) {
                putpixel(x + j, y + i, VGA_COLOR);
            }
        }
    }
}

void print_string(const char *str, int x, int y, u8 VGA_COLOR) {
    int offset = 0;
    while (*str) {
        char ch = *str++;
        if (ch == '\n') {
            y += FONT_SIZE;
            offset = 0;
        } else {
            draw_char(ch, x + offset, y, VGA_COLOR);
            offset += FONT_SIZE;
        }
    }
}
